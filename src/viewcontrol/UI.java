    private void btnBrowseTrainingDataFolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseTrainingDataFolderActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File("G:\\Training data"));
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.showOpenDialog(this);
        trainingDataFolder = fileChooser.getSelectedFile();
        if (trainingDataFolder != null) {
            txtTrainingDataFolderPath.setText(trainingDataFolder.getAbsolutePath());
        }
    }//GEN-LAST:event_btnBrowseTrainingDataFolderActionPerformed

    private void btnTrainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrainActionPerformed
        if (trainingDataFolder == null) {
            JOptionPane.showMessageDialog(this, "Please choose training data folder!");
            return;
        }
        try {
            String trainingStatus = trainer.train(trainingDataFolder);
            txaTrainingStatus.setText(trainingStatus);
        } catch (IOException ex) {
            Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTrainActionPerformed

