    private void btnTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestActionPerformed
        if (testingDataFolder == null) {
            JOptionPane.showMessageDialog(this, "Please choose testing data folder!");
            return;
        }
        try {
            String testingResult = classifier.test(testingDataFolder);
            txaTestingResult.setText(testingResult);
        } catch (IOException ex) {
            Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTestActionPerformed

    private void btnClassifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClassifyActionPerformed
        String inputSentence = txaInputSentence.getText();
        if (inputSentence.length() > 0) {
            try {
                classifier.classify(inputSentence);
                Scanner scanner = new Scanner(new FileInputStream(Config.SVM_OUPUT));
                String line = scanner.nextLine();
                int index = Integer.parseInt(line.substring(0, line.indexOf(" ")));
                String emotion = Config.EMOTIONS[index - 1];
                JOptionPane.showMessageDialog(this, emotion);
            } catch (IOException ex) {
                Logger.getLogger(UI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnClassifyActionPerformed

